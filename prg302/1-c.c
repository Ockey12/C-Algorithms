#include<stdio.h>

int main(void)
{
  int m = 738, n = 196;
	
  printf("The expressions shown below are correct.\n");
  printf("%d + %d = %d\n", m, n, m + n);
  printf("%d - %d = %d\n", m, n, m - n);
  printf("%d * %d = %d\n", m, n, m * n);
  printf("%d / %d = %d\n", m, n, m / n);
  printf("%d %% %d = %d\n", m, n, m % n);
	
  return 0;
}
